def ConvertHexToRGB(code: int):
    return tuple(int(code[i:i+2], 16) for i in (0, 2, 4))
def ConvertRGBToHex(code: tuple):
    return "{:02x}{:02x}{:02x}".format(code[0], code[1], code[2])